//Fahrenheit to celsius conversion
let tempInFahrenheit = 100

let celsius = (tempInFahrenheit - 32) * 5/9

console.log('temperature in celsius is:')
console.log(celsius)

//Celsius to Fahrenheit conversion

let tempInCelsius = 40

let fahrenheit = (tempInCelsius * 5/9) + 32

console.log('temperature in fahrenheit is:')
console.log(fahrenheit)